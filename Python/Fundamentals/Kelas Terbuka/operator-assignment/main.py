# penjelasan: merupakan unary operation untuk satu variable itu sendiri

x = 2
print(x)

# penjumlahan
x+=2
print(x)

# pengurangan
x-=2
print(x)

# perkalian
x*=50
print(x)

# pembagian
x/=10
print(x)

# eksponensial
x**=2
print(x)

# floor division
x//=3
print(x)

# modulo
x%=2
print(x)

# bitwise or
a = True
a |= False
print(a)

# bitwise and
a = False
a &= True
print(a)

# bitwise xor
a = True
a ^= True
print(a)

# bitwise shift left
x = 12
print(format(x, '08b'))
x<<=2
print(format(x, '08b'))

# bitwise shift right
y = 24
print(format(y, '08b'))
y>>=3
print(format(y, '08b'))